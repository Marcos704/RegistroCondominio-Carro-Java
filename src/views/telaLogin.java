    private void btMostrarSenha1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btMostrarSenha1ActionPerformed
        // TODO add your handling code here:
        if(inputUserName.getText().equals("") && !inputPassword.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Veficar Dados\n"+"*Usuário: Não Inserido!"+"\n*Senha: "+inputPassword.getText());
        }else
            if(!inputUserName.getText().equals("") && inputPassword.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Verificar Dados\n*Usuário: "+inputUserName.getText()+"\n*Senha: Não Inserido!");
            }
            else{
                if(!inputUserName.getText().equals("") && !inputPassword.getText().equals("")){
                  JOptionPane.showMessageDialog(this, "Verificar Dados\n *Nome: "+inputUserName.getText()+"\n*Senha: "+inputPassword.getText());
                }
            }
    }//GEN-LAST:event_btMostrarSenha1ActionPerformed

