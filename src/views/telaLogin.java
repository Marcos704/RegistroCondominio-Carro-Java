    private void btLoguinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLoguinActionPerformed
        // TODO add your handling code here:
        try{
           
             int K1, K2;
        Loguin data = new Loguin(inputUserName.getText(), inputPassword.getText());
        K1 = data.mVerificarLoginUser(inputUserName.getText());
        K2 = data.VerificarLoginSenha(inputPassword.getText());
        
        if(K1 == 1 && K2 ==1){
            menuInicial menu = new menuInicial();
            menu.setVisible(true);
            dispose();
        }else
            if(K1 == 0 && K2 == 0){
               JOptionPane.showMessageDialog(data, "|OP|->Erro 002\n Não forma encontrados usuários\ncom esses dados.");
             
            }else
                if(K1 !=0 && K2 ==0){
                    JOptionPane.showMessageDialog(data, "|OP|-> Erro 003.\n Verifique os dados.\nVeja se estão corretos.\n(Ajuda)->Senha Incorreta.");
                }else
                    if(K1 == 0 && K2 != 0){
                        JOptionPane.showMessageDialog(data, "|OP|-> Erro 004.\n Verifique os dados.\nVeja se estão corretos.\n(Ajuda)->Usuário Incorreto.");
                    }
            
        }catch(HeadlessException A){
            System.exit(0);
        }
    }//GEN-LAST:event_btLoguinActionPerformed

